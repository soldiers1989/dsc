package com.yixin.kepler.v1.datapackage.dto.yillion;

import java.math.BigDecimal;

/**
 * @description: 亿联请款数据结构
 * @date: 2018-11-01 14:16
 */
public class YILLIONPaymentDTO {

    /**
     * 订单编号
     */
    private String order_no;
    /**
     * 外部交易流水号
     */
    private String extxnseq;
    /**
     * 放款金额
     */
    private BigDecimal loanamt;
    /**
     * 放款期限
     */
    private String loanterm;
    /**
     * 还款方式
     */
    private String loantype;


    public String getOrder_no() {
        return order_no;
    }

    public void setOrder_no(String order_no) {
        this.order_no = order_no;
    }

    public String getExtxnseq() {
        return extxnseq;
    }

    public void setExtxnseq(String extxnseq) {
        this.extxnseq = extxnseq;
    }

    public BigDecimal getLoanamt() {
        return loanamt;
    }

    public void setLoanamt(BigDecimal loanamt) {
        this.loanamt = loanamt;
    }

    public String getLoanterm() {
        return loanterm;
    }

    public void setLoanterm(String loanterm) {
        this.loanterm = loanterm;
    }

    public String getLoantype() {
        return loantype;
    }

    public void setLoantype(String loantype) {
        this.loantype = loantype;
    }
}
