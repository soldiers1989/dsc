import com.yixin.kepler.dto.OsbAttachmentDTO;

/**
 * Package : com.yixin.kepler.core.attachment
 *
 * @author YixinCapital -- liushuai2
 *         2018年06月06日 17:47
 */
public class Attachment extends OsbAttachmentDTO {

    private String attachmentUrl;
    private String attachmentName;
    private String attachmentType;
    private String attachmentSize;

    public Attachment(){

    }

    public Attachment(String attachmentUrl, String attachmentName, String attachmentType, String attachmentSize) {
        this.attachmentUrl = attachmentUrl;
        this.attachmentName = attachmentName;
        this.attachmentType = attachmentType;
        this.attachmentSize = attachmentSize;
    }

    public String getAttachmentUrl() {
        return attachmentUrl;
    }

    public void setAttachmentUrl(String attachmentUrl) {
        this.attachmentUrl = attachmentUrl;
    }

    public String getAttachmentName() {
        return attachmentName;
    }

    public void setAttachmentName(String attachmentName) {
        this.attachmentName = attachmentName;
    }

    public String getAttachmentType() {
        return attachmentType;
    }

    public void setAttachmentType(String attachmentType) {
        this.attachmentType = attachmentType;
    }

    public String getAttachmentSize() {
        return attachmentSize;
    }

    public void setAttachmentSize(String attachmentSize) {
        this.attachmentSize = attachmentSize;
    }
}
