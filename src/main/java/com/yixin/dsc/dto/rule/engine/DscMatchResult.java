package com.yixin.dsc.dto.rule.engine;

import java.util.Date;
import java.util.Map;

/**
 * 规则匹配结果
 * Package : com.yixin.dsc.dto.rule.engine
 * 
 * @author YixinCapital -- huguoxing
 *		   2018年6月6日 下午7:23:20
 *
 */
public class DscMatchResult {
	
	//规则id
	private String ruleId;
	
	//规则明细ID
	private String ruleDetailId;
	
	//是否匹配
	private boolean isMach ;
	
	//规则返回
	private String returnStr;
	
	//规则返回数据类别(材料、字段等 SupplyTypeEnum)
	private String supplyType;
	
	/**
	 * 匹配时间
	 */
	private Date matchTime;
	
	/**
	 * 规则公式定义
	 */
	private String ruleFormula;
	
	/**
	 * 规则message
	 */
	private String ruleMessage;
	
	private Map<String,Object> fieldValues;
	
	/**
	 * 匹配结果MAP
	 */
	private Map<String,Object> matchResultMap;
	
	public Map<String, Object> getMatchResultMap() {
		return matchResultMap;
	}

	public void setMatchResultMap(Map<String, Object> matchResultMap) {
		this.matchResultMap = matchResultMap;
	}

	public Map<String, Object> getFieldValues() {
		return fieldValues;
	}

	public void setFieldValues(Map<String, Object> fieldValues) {
		this.fieldValues = fieldValues;
	}

	public String getRuleMessage() {
		return ruleMessage;
	}

	public void setRuleMessage(String ruleMessage) {
		this.ruleMessage = ruleMessage;
	}

	public Date getMatchTime() {
		return matchTime;
	}

	public void setMatchTime(Date matchTime) {
		this.matchTime = matchTime;
	}

	public String getRuleDetailId() {
		return ruleDetailId;
	}

	public void setRuleDetailId(String ruleDetailId) {
		this.ruleDetailId = ruleDetailId;
	}

	public String getRuleFormula() {
		return ruleFormula;
	}

	public void setRuleFormula(String ruleFormula) {
		this.ruleFormula = ruleFormula;
	}

	public String getRuleId() {
		return ruleId;
	}

	public void setRuleId(String ruleId) {
		this.ruleId = ruleId;
	}

	public boolean isMach() {
		return isMach;
	}

	public void setMach(boolean isMach) {
		this.isMach = isMach;
	}

	public String getReturnStr() {
		return returnStr;
	}

	public void setReturnStr(String returnStr) {
		this.returnStr = returnStr;
	}

	public String getSupplyType() {
		return supplyType;
	}

	public void setSupplyType(String supplyType) {
		this.supplyType = supplyType;
	}
	
}
