package com.yixin.dsc.dto;

import java.io.Serializable;

/**
 * 工作流逆向
 * 取消、提回、退回订单
 * 
 * Package : com.yixin.dsc.dto
 * 
 * @author YixinCapital -- wangxulong
 *		   2018年6月3日 下午9:50:15
 *
 */
public class DscReverseFlowForAlixDto implements Serializable{
	
	
	private static final long serialVersionUID = 7867889412012600645L;

	/**
	 * 动作类型
	 * 1：取消，2：提回 3：退回 4 拒绝
	 */
	private String ctype;
	
	/**
	 * 订单编号
	 */
	private String applyNo;
	
	/**
	 * 受理结果code，成功：100000，失败：100001
	 */
	private String code;
	
	/**
	 * 处理结果message
	 */
	private String message;
	
	


	public String getCtype() {
		return ctype;
	}

	public void setCtype(String ctype) {
		this.ctype = ctype;
	}

	public String getApplyNo() {
		return applyNo;
	}

	public void setApplyNo(String applyNo) {
		this.applyNo = applyNo;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}