public class YNTrustMaintenanceDTO implements Serializable {

	private static final long serialVersionUID = 385202009341290478L;
	
	/** 订单编号  */
	private String applyNo;
	
	/** 产品代码   */
	private String productCode;
		
	/** 虚拟子账号   */
	private String subAccount;
	
	/** 每笔贷款唯一标示   */
	private String uniqueId;
	
	/** 银行返回的报文 */
	private String bankResponseBody;
	
	/** 维护的类型  */
	private String maintenanceType;
	

	public String getBankResponseBody() {
		return bankResponseBody;
	}

	public void setBankResponseBody(String bankResponseBody) {
		this.bankResponseBody = bankResponseBody;
	}

	public String getMaintenanceType() {
		return maintenanceType;
	}

	public void setMaintenanceType(String maintenanceType) {
		this.maintenanceType = maintenanceType;
	}

	public String getApplyNo() {
		return applyNo;
	}

	public void setApplyNo(String applyNo) {
		this.applyNo = applyNo;
	}

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}

	public String getSubAccount() {
		return subAccount;
	}

	public void setSubAccount(String subAccount) {
		this.subAccount = subAccount;
	}

	public String getUniqueId() {
		return uniqueId;
	}

	public void setUniqueId(String uniqueId) {
		this.uniqueId = uniqueId;
	}
}
