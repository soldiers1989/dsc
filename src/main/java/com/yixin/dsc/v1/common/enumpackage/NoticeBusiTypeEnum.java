public enum NoticeBusiTypeEnum {

	CONTRACT_SIGNATURE("CONTRACT_SIGNATURE","合同签章");

	/**
	 * 业务类型码值
	 */
	private String busiType;
	/**
	 * 业务类型描述
	 */
    private String describe;
    
	private NoticeBusiTypeEnum() {}
	
	private NoticeBusiTypeEnum(String busiType, String describe) {
		this.busiType = busiType;
		this.describe = describe;
	}

	public String getBusiType() {
		return busiType;
	}

	public String getDescribe() {
		return describe;
	}
}
